/*
 * This algorithm Uses Paul Bourke's tabulation approach to marching cubes
 * http://paulbourke.net/geometry/polygonise/
 *
 * We model cubes with 8 vertices labelled as below
 *
 *
 *        4---------5
 *       /|        /|
 *      / |       / |
 *     /  |      /  |
 *    7---------6   |
 *    |   0-----|---1
 *    |  /      |  /
 *    | /       | /
 *    |/        |/
 *    3---------2
 *
 * vertices of each cube are labelled 0 to 7 as follows:
 *
 * 0: ( x, y, z+1 )     4: ( x, y+1, z+1 )
 * 1: ( x+1, y, z+1 )   5: ( x+1, y+1, z+1 )
 * 2: ( x+1, y, z )     6: ( x+1, y+1, z )
 * 3: ( x, y, z )       7: ( x, y+1, z )
 *
 * Tis table gives the offset voxels for each vetex of the cube
 */
